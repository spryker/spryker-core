namespace Spryker\Zed\MerchantRegistrationRequest\Dependency\Service;

class MerchantRegistrationRequestToUtilTextServiceBridge implements MerchantRegistrationRequestToUtilTextServiceInterface
{
    /**
     * @var \Spryker\Service\UtilText\UtilTextServiceInterface
     */
    protected $utilTextService;

    /**
     * @param \Spryker\Service\UtilText\UtilTextServiceInterface $utilTextService
     */
    public function __construct($utilTextService)
    {
        $this->utilTextService = $utilTextService;
    }

    public function generateSlug(string $text): string
    {
        return $this->utilTextService->generateSlug($text);
    }
}
