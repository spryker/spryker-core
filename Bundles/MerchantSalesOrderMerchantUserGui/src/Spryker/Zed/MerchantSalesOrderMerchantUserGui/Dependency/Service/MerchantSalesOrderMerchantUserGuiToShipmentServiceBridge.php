namespace Spryker\Zed\MerchantSalesOrderMerchantUserGui\Dependency\Service;

use ArrayObject;

class MerchantSalesOrderMerchantUserGuiToShipmentServiceBridge implements MerchantSalesOrderMerchantUserGuiToShipmentServiceInterface
{
    /**
     * @var \Spryker\Service\Shipment\ShipmentServiceInterface
     */
    protected $shipmentService;

    /**
     * @param \Spryker\Service\Shipment\ShipmentServiceInterface $shipmentService
     */
    public function __construct($shipmentService)
    {
        $this->shipmentService = $shipmentService;
    }

    /**
     * @phpstan-return \ArrayObject<int, \Generated\Shared\Transfer\ShipmentGroupTransfer>
     *
     * @param iterable|\Generated\Shared\Transfer\ItemTransfer[] $itemTransferCollection
     *
     * @return \ArrayObject|\Generated\Shared\Transfer\ShipmentGroupTransfer[]
     */
    public function groupItemsByShipment(iterable $itemTransferCollection): ArrayObject
    {
        return $this->shipmentService->groupItemsByShipment($itemTransferCollection);
    }
}
