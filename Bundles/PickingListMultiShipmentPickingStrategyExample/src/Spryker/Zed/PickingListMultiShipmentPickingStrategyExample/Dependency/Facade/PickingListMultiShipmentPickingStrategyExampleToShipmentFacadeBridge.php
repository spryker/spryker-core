namespace Spryker\Zed\PickingListMultiShipmentPickingStrategyExample\Dependency\Facade;

class PickingListMultiShipmentPickingStrategyExampleToShipmentFacadeBridge implements PickingListMultiShipmentPickingStrategyExampleToShipmentFacadeInterface
{
    /**
     * @var \Spryker\Zed\Shipment\Business\ShipmentFacadeInterface
     */
    protected $shipmentFacade;

    /**
     * @param \Spryker\Zed\Shipment\Business\ShipmentFacadeInterface $shipmentFacade
     */
    public function __construct($shipmentFacade)
    {
        $this->shipmentFacade = $shipmentFacade;
    }

    /**
     * @param list<\Generated\Shared\Transfer\ItemTransfer> $itemTransfers
     *
     * @return list<\Generated\Shared\Transfer\ItemTransfer>
     */
    public function expandOrderItemsWithShipment(array $itemTransfers): array
    {
        return $this->shipmentFacade->expandOrderItemsWithShipment($itemTransfers);
    }
}
