namespace Spryker\Client\PriceProductMerchantRelationship\Dependency\Client;

class PriceProductMerchantRelationshipToCartClientBridge implements PriceProductMerchantRelationshipToCartClientInterface
{
    /**
     * @var \Spryker\Client\Cart\CartClientInterface
     */
    protected $cartClient;

    /**
     * @param \Spryker\Client\Cart\CartClientInterface $cartClient
     */
    public function __construct($cartClient)
    {
        $this->cartClient = $cartClient;
    }

    /**
     * @return void
     */
    public function reloadItems(): void
    {
        $this->cartClient->reloadItems();
    }
}
